def ref_ece(p, y, bins):
    p, y = np.array(p), np.array(y)
    idx = np.clip(np.ceil(p * bins).astype(int) - 1, 0, bins - 1)
    total = 0.0
    for b in range(bins):
        m = idx == b
        if m.any():
            total += abs(y[m].mean() - p[m].mean()) * m.sum() / len(p)
    return total


