# ---------------- deterministic data (64-bit LCG, 53-bit uniforms) ----------------

def uniforms(seed, n, lo=0.0, hi=1.0):
    state = seed & ((1 << 64) - 1)
    out = []
    for _ in range(n):
        state = (state * 6364136223846793005 + 1442695040888963407) % (1 << 64)
        out.append(lo + (hi - lo) * (state >> 11) / float(1 << 53))
    return out


